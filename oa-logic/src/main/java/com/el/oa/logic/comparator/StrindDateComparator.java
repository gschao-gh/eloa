public class StrindDateComparator implements Comparator<String> {


    @Override
    public int compare(String o1, String o2) {
        try {
            int first = DateUtils.stringDateToInt(o1);
            int second = DateUtils.stringDateToInt(o2);
            return first-second;
        } catch (ParseException e) {
            e.printStackTrace();
        }
        return 0;
    }
}
