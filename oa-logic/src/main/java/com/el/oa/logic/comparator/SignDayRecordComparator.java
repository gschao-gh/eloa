public class SignDayRecordComparator implements Comparator<SignDayRecord> {


    @Override
    public int compare(SignDayRecord o1, SignDayRecord o2) {
        try {
            int first = DateUtils.stringDayToInt(o1.getDay());
            int second = DateUtils.stringDayToInt(o2.getDay());
            return first-second;
        } catch (ParseException e) {
            e.printStackTrace();
        }
        return 0;
    }
}
